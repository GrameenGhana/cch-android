package org.grameenfoundation.cch.model;

public class TargetChild {
	
	private String target_name;
	private String target_number;
	private String target_achieved;
	private String target_start;
	private String target_end;
	private String target_type;
	    private String Tag;
	    
	    public String getTargetName() {
	        return target_name;
	    }
	    public void setTargetName(String target_name) {
	        this.target_name = target_name;
	    }
	    
	    public String getTargetNumber() {
	        return target_number;
	    }
	    public void setTargetNumber(String target_number) {
	        this.target_number = target_number;
	    }
	    
	    public String getTargetAchieved() {
	        return target_achieved;
	    }
	    public void setTargetAchieved(String target_achieved) {
	        this.target_achieved = target_achieved;
	    }
	    
	    public String getTargetStart() {
	        return target_start;
	    }
	    public void setTargetStart(String target_start) {
	        this.target_start = target_start;
	    }
	    
	    public String getTargetEnd() {
	        return target_end;
	    }
	    public void setTargetEnd(String target_end) {
	        this.target_end = target_end;
	    }
	    
	    public String getTargetType() {
	        return target_type;
	    }
	    public void setTargetType(String target_type) {
	        this.target_type = target_type;
	    }
	    
	    public String getTag() {
	        return Tag;
	    }
	    public void setTag(String Tag) {
	        this.Tag = Tag;
	    }

}
